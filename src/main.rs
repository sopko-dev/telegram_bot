    // –ö–†–ò–¢–ò–ß–ù–ê –ü–ï–†–ï–í–Ü–†–ö–ê: –ù–ï –í–Ü–î–ü–û–í–Ü–î–ê–¢–ò –ù–ê –í–õ–ê–°–ù–Ü –ü–û–í–Ü–î–û–ú–õ–ï–ù–ù–Ø
