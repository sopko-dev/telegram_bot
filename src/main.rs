    // warp::serve(health_route) - —Å—Ç–≤–æ—Ä—é—î–º–æ —Å–µ—Ä–≤–µ—Ä –∑ –Ω–∞—à–∏–º –º–∞—Ä—à—Ä—É—Ç–æ–º
    // .run(([0, 0, 0, 0], port)) - –∑–∞–ø—É—Å–∫–∞—î–º–æ —Å–µ—Ä–≤–µ—Ä –Ω–∞ –≤—Å—ñ—Ö –º–µ—Ä–µ–∂–µ–≤–∏—Ö —ñ–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞—Ö (0.0.0.0) –Ω–∞ –≤–∫–∞–∑–∞–Ω–æ–º—É –ø–æ—Ä—Ç—É
    // .await - —á–µ–∫–∞—î–º–æ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è —Ä–æ–±–æ—Ç–∏ —Å–µ—Ä–≤–µ—Ä–∞ (–≤—ñ–Ω –ø—Ä–∞—Ü—é–≤–∞—Ç–∏–º–µ –ø–æ—Å—Ç—ñ–π–Ω–æ)
    // –¶–µ–π —Å–µ—Ä–≤–µ—Ä –ø—Ä–∞—Ü—é—î –ø–∞—Ä–∞–ª–µ–ª—å–Ω–æ –∑ –±–æ—Ç–æ–º —ñ –≤—ñ–¥–ø–æ–≤—ñ–¥–∞—î –Ω–∞ health check –∑–∞–ø–∏—Ç–∏
