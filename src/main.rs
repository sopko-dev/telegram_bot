    // –î—É–∂–µ –≤–∞–∂–ª–∏–≤–æ –¥–ª—è –¥—ñ–∞–≥–Ω–æ—Å—Ç–∏–∫–∏ –ø—Ä–æ–±–ª–µ–º –ø—ñ–¥ —á–∞—Å —Ä–æ–∑—Ä–æ–±–∫–∏ —Ç–∞ —Ä–æ–±–æ—Ç–∏ –±–æ—Ç–∞
    pretty_env_logger::init();

    // –õ–û–ì–£–í–ê–ù–ù–Ø –ü–û–ß–ê–¢–ö–£ –†–û–ë–û–¢–ò
    // log::info! - –º–∞–∫—Ä–æ—Å –¥–ª—è –≤–∏–≤–µ–¥–µ–Ω–Ω—è —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ–π–Ω–æ–≥–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
    // "Starting bot..." - —Ç–µ–∫—Å—Ç –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è, —è–∫–∏–π –∑'—è–≤–∏—Ç—å—Å—è –≤ –∫–æ–Ω—Å–æ–ª—ñ
    // –¶–µ –¥–æ–ø–æ–º–æ–∂–µ –∑—Ä–æ–∑—É–º—ñ—Ç–∏, —â–æ –±–æ—Ç –ø–æ—á–∞–≤ –∑–∞–ø—É—Å–∫–∞—Ç–∏—Å—è
    log::info!("Starting bot...");

    // ============================================================================
    // –ù–ê–õ–ê–®–¢–£–í–ê–ù–ù–Ø –ü–û–†–¢–£ –î–õ–Ø RENDER.COM (PORT SETUP FOR RENDER)
    // ============================================================================
    // Render.com - —Ü–µ —Ö–æ—Å—Ç–∏–Ω–≥-–ø–ª–∞—Ç—Ñ–æ—Ä–º–∞, –¥–µ –±–æ—Ç –±—É–¥–µ –ø—Ä–∞—Ü—é–≤–∞—Ç–∏ 24/7
