    log::info!("Web server started on port {}", port);

    // ============================================================================
    // –ù–ê–õ–ê–®–¢–£–í–ê–ù–ù–Ø –ë–û–¢–ê (BOT SETUP)
    // ============================================================================
    // –¢—É—Ç –º–∏ —Å—Ç–≤–æ—Ä—é—î–º–æ –æ–±'—î–∫—Ç –±–æ—Ç–∞, —è–∫–∏–π –±—É–¥–µ —Å–ø—ñ–ª–∫—É–≤–∞—Ç–∏—Å—å –∑ Telegram API

    // std::env::var("TELEGRAM_BOT_TOKEN") - –æ—Ç—Ä–∏–º—É—î–º–æ —Ç–æ–∫–µ–Ω –±–æ—Ç–∞ –∑—ñ –∑–º—ñ–Ω–Ω–æ—ó –æ—Ç–æ—á–µ–Ω–Ω—è
    // –¶–µ–π —Ç–æ–∫–µ–Ω –≥–µ–Ω–µ—Ä—É—î—Ç—å—Å—è @BotFather –≤ Telegram —ñ –≤–∏–≥–ª—è–¥–∞—î —è–∫: 123456:ABC-DEF1234567890
    // .expect("TELEGRAM_BOT_TOKEN environment variable not set") - —è–∫—â–æ –∑–º—ñ–Ω–Ω–∞ –Ω–µ –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞,
    // –∑—É–ø–∏–Ω—è—î–º–æ –ø—Ä–æ–≥—Ä–∞–º—É –∑ —Ü–∏–º –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è–º –ø—Ä–æ –ø–æ–º–∏–ª–∫—É
