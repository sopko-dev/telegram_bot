    // () - –ø–æ—Ä–æ–∂–Ω—ñ –¥—É–∂–∫–∏ –æ–∑–Ω–∞—á–∞—é—Ç—å, —â–æ –º–µ—Ç–æ–¥ –Ω–µ –ø–æ—Ç—Ä–µ–±—É—î –ø–∞—Ä–∞–º–µ—Ç—Ä—ñ–≤
    // { - –≤—ñ–¥–∫—Ä–∏–≤–∞—é—á–∞ —Ñ—ñ–≥—É—Ä–Ω–∞ –¥—É–∂–∫–∞ –æ–∑–Ω–∞—á–∞—î –ø–æ—á–∞—Ç–æ–∫ –±–ª–æ–∫—É –∫–æ–¥—É, —è–∫–∏–π –≤–∏–∫–æ–Ω–∞—î—Ç—å—Å—è, —è–∫—â–æ —Ç–µ–∫—Å—Ç —î
