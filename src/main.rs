    // ============================================================================
    // Render –ø–µ—Ä–µ–≤—ñ—Ä—è—î, —á–∏ –∂–∏–≤–∏–π –Ω–∞—à –¥–æ–¥–∞—Ç–æ–∫, –Ω–∞–¥—Å–∏–ª–∞—é—á–∏ HTTP –∑–∞–ø–∏—Ç–∏ –Ω–∞ –ø–µ–≤–Ω—É –∞–¥—Ä–µ—Å—É
    // –Ø–∫—â–æ –¥–æ–¥–∞—Ç–æ–∫ –Ω–µ –≤—ñ–¥–ø–æ–≤—ñ–¥–∞—î, Render –≤–≤–∞–∂–∞—î –π–æ–≥–æ "—Å–ø–ª—è—á–∏–º" —ñ –º–æ–∂–µ –π–æ–≥–æ –∑—É–ø–∏–Ω–∏—Ç–∏

    // warp::path::end() - —Å—Ç–≤–æ—Ä—é—î –º–∞—Ä—à—Ä—É—Ç –¥–ª—è –∫–æ—Ä–µ–Ω–µ–≤–æ–≥–æ —à–ª—è—Ö—É "/" (–≥–æ–ª–æ–≤–Ω–∞ —Å—Ç–æ—Ä—ñ–Ω–∫–∞)
    // .map(|| "Alive") - –¥–ª—è —Ü—å–æ–≥–æ –º–∞—Ä—à—Ä—É—Ç—É –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ —Ä—è–¥–æ–∫ "Alive"
    // –¶–µ –æ–∑–Ω–∞—á–∞—î, —â–æ –∫–æ–ª–∏ —Ö—Ç–æ—Å—å –∑–∞–π–¥–µ –Ω–∞ https://–Ω–∞—à_–±–æ—Ç.render.com/, –≤—ñ–Ω –ø–æ–±–∞—á–∏—Ç—å "Alive"
