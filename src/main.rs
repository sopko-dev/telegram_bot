    // ============================================================================
    // –ù–ê–õ–ê–®–¢–£–í–ê–ù–ù–Ø –û–ë–†–û–ë–ù–ò–ö–ê –ü–û–í–Ü–î–û–ú–õ–ï–ù–¨ (MESSAGE HANDLER SETUP)
    // ============================================================================
    // –°—Ç–≤–æ—Ä—é—î–º–æ —Å–∏—Å—Ç–µ–º—É –¥–ª—è –æ–±—Ä–æ–±–∫–∏ —Ä—ñ–∑–Ω–∏—Ö —Ç–∏–ø—ñ–≤ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω—å –≤—ñ–¥ Telegram

    // dptree::entry() - —Å—Ç–≤–æ—Ä—é—î–º–æ —Ç–æ—á–∫—É –≤—Ö–æ–¥—É –¥–ª—è –¥–µ—Ä–µ–≤–∞ –æ–±—Ä–æ–±–Ω–∏–∫—ñ–≤
