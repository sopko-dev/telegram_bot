// –ë–µ–∑ tokio –±–æ—Ç –ø—Ä–∞—Ü—é–≤–∞–≤ –±–∏ –¥—É–∂–µ –ø–æ–≤—ñ–ª—å–Ω–æ —ñ "–∑–∞–≤–∏—Å–∞–≤" –Ω–∞ –∫–æ–∂–Ω—ñ–π –æ–ø–µ—Ä–∞—Ü—ñ—ó
#[tokio::main]
